package packej.cj;

public class Staff {
	int sid;
	String sneme;
	double ssalary;
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSneme() {
		return sneme;
	}
	public void setSneme(String sneme) {
		this.sneme = sneme;
	}
	public double getSsalary() {
		return ssalary;
	}
	public void setSsalary(double ssalary) {
		this.ssalary = ssalary;
	}
	

}
